# CRIE UMA EXPRESSAO REGULAR QUE FACA O CASAMENTO DE PADRAO
# COM O NUMERO DE TELEFONE PRESENTE NO TEXTO A SEGUIR.
# "OLÁ, TUDO BEM? MEU WHATSAPP É (99) 74321-1234"

texto = "OLÁ, TUDO BEM? MEU WHATSAPP É (99) 74321-1234"
numero_telefone = texto.match(/\(\d{2}\) \d{5}-\d{4}/)[0]
puts numero_telefone
# Saída: (99) 74321-1234
